package enums;

/**
 * Represents the registration status of an officer.
 */
public enum OfficerRegistrationStatus {
    /**
     * The officer's registration is pending approval.
     */
    PENDING,

    /**
     * The officer's registration has been approved.
     */
    APPROVED,

    /**
     * The officer's registration has been rejected.
     */
    REJECTED
}
